package Data;

public class ReturnedUser {
	
	public String user;
	public String profile;
	public String picture;
	public String email; 
	
	public ReturnedUser(String user, String profile, String picture, String email) {
		this.user = user;
		this.profile = profile;
		this.picture = picture;
		this.email = email;
	}
}
